// ============================================================================
// sync-canonicalizer — regenerate the canonicalizer mirrors from the SSOT.
// ----------------------------------------------------------------------------
// Single source of truth: packages/kinetik-core/src/stableJson.ts
//   export const stableStringify(obj) — shallow lex-sorted JSON serializer.
//
// stableStringify produces the EXACT bytes that every signable artifact
// (Proof of Origin, signed heartbeat, signed earning, bureau attestation) is
// signed over. Any byte drift between copies breaks Ed25519 verification for
// previously-minted artifacts. To guarantee one implementation, the
// non-app consumers below do NOT hand-author their own copy — they consume a
// GENERATED mirror of the SSOT, drift-guarded in CI (mirrors the
// scripts/sync-worker-policy.mjs pattern PR 2 established for the policy).
//
// Generated mirrors:
//   · functions/api/_lib/stable-json.js   — Cloudflare Pages Functions (ESM,
//     zero-dependency; matches the _lib/*.js convention).
//   · landing/verify/stable-json.js        — public browser verifier (static
//     ESM module, ZERO imports, so landing/verify stays dependency-free under
//     the strict `script-src 'self'` CSP).
//   · packages/verify/src/stableJson.ts    — published @getkinetik/verify SDK
//     (TypeScript; compiled into dist so the package stays self-contained with
//     no runtime dependency on the private, unbuilt @kinetik/core package).
//
// Run:  npm run canonical:sync
// CI reruns this and fails (git diff --exit-code) if any mirror is stale.
// ============================================================================

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");

const SSOT = resolve(repoRoot, "packages/kinetik-core/src/stableJson.ts");

const TARGETS = {
  worker: resolve(repoRoot, "functions/api/_lib/stable-json.js"),
  browser: resolve(repoRoot, "landing/verify/stable-json.js"),
  verifyPkg: resolve(repoRoot, "packages/verify/src/stableJson.ts"),
};

// Captures the whole `stableStringify` definition (const-arrow or function
// form) from declaration through the canonical `return` statement. Anchored on
// the exact return shape so any change to the SSOT algorithm forces a
// deliberate update here rather than silently emitting a malformed mirror.
const FN_RE =
  /(?:export\s+)?(?:const\s+stableStringify\s*=\s*\(obj[^)]*\)\s*(?::[^=]*)?=>\s*\{|function\s+stableStringify\s*\(obj[^)]*\)\s*\{)[\s\S]*?return\s*`\{\$\{parts\.join\([^)]*\)\}\}`\s*;\s*\}/;

function extractRawFn(text) {
  const m = text.match(FN_RE);
  if (!m) {
    console.error(
      `[sync-canonicalizer] could not locate stableStringify in the SSOT\n` +
        `  ${SSOT}\n` +
        `If the SSOT was intentionally refactored, update FN_RE in this script.`,
    );
    process.exit(1);
  }
  return m[0].replace(/\r\n/g, "\n").trim();
}

// Strip the (few, known) TypeScript annotations so the function is valid JS.
function toJs(rawFn) {
  return rawFn
    .replace(/^export\s+/, "")
    .replace(/\(obj[^)]*\)/, "(obj)")
    .replace(/\)\s*:[^=]*=>/, ") =>")
    .replace(/const\s+parts\s*:[^=]*=/, "const parts =");
}

const BANNER = `/**
 * GENERATED FILE
 * Source: packages/kinetik-core/src/stableJson.ts
 * Generated by: npm run canonical:sync
 * DO NOT EDIT
 */`;

let ssotText;
try {
  ssotText = readFileSync(SSOT, "utf8");
} catch {
  console.error(`[sync-canonicalizer] could not read SSOT: ${SSOT}`);
  process.exit(1);
}

const rawFn = extractRawFn(ssotText);
const jsFn = toJs(rawFn);

// Worker + browser mirrors: identical zero-import ESM JS.
const jsMirror = `${BANNER}\n\nexport ${jsFn.replace(/^export\s+/, "")}\n`;

// Verify SDK mirror: keep TypeScript types so it compiles cleanly in the
// package's strict tsc build. rawFn already carries `export`.
const tsMirror = `${BANNER}\n\n${rawFn}\n`;

const writes = [
  [TARGETS.worker, jsMirror],
  [TARGETS.browser, jsMirror],
  [TARGETS.verifyPkg, tsMirror],
];

let changed = 0;
for (const [out, content] of writes) {
  let existing = null;
  try {
    existing = readFileSync(out, "utf8").replace(/\r\n/g, "\n");
  } catch {
    existing = null;
  }
  if (existing === content) {
    console.log(`[sync-canonicalizer] up to date: ${out}`);
    continue;
  }
  writeFileSync(out, content, "utf8");
  console.log(`[sync-canonicalizer] wrote ${out}`);
  changed++;
}

console.log(
  `[sync-canonicalizer] done (${changed} file(s) updated, ${writes.length - changed} unchanged)`,
);
